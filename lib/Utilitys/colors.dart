List<Color> myLibraryBooksColors(int length) {
  var arrValues = [
    const Color.fromARGB(255, 223, 244, 243),
    const Color.fromARGB(230, 132, 219, 218),
    const Color.fromARGB(255, 250, 228, 130),
    const Color.fromARGB(255, 204, 223, 151),
    const Color.fromARGB(255, 250, 221, 197),
    const Color.fromARGB(255, 181, 150, 222),
    const Color.fromARGB(255, 151, 223, 215),
    const Color.fromARGB(255, 133, 218, 252),
    const Color.fromARGB(255, 153, 236, 192),
    const Color.fromARGB(255, 210, 210, 255),
    const Color.fromARGB(255, 250, 192, 195)
  ];
  if (arrValues.length > length) {
    return arrValues;
  } else {
    for (int i = 0; i < length; i++) {
      if (i == length || i > arrValues.length) {
        arrValues.add(i % 2 == 0
            ? (arrValues..shuffle()).first
            : (arrValues..shuffle()).last);
      }
    }
  }
  return arrValues;
}

